# Operador lógico "not"
# Usado para inverter expressões
# not True = False
# not False = True

mentiroso = True  # O mentiroso diz que é verdade

print(not mentiroso)  # Se o mentiroso mente, e diz que é verdade, logo é mentira = False

mentiroso2 = False  # O outro mentiroso diz que é mentira

print(not mentiroso2)  # O que ele diz que é mentira, na realidade é verdade = True