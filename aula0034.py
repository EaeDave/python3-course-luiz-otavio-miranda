"""
Repetições
while (enquanto)
Executa uma ação enquanto uma condição for verdadeira
"""

# Criando um for dentro de um while
valor = 0
while valor < 11:
    print(valor)
    valor += 1

# Looping infinito interrompido

while True:
    print('Isso seria impressoro inifitamente se não fosse o break.')
    break