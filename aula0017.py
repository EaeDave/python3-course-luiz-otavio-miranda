# if / elif       / else
# se / se não se  / se não

condicao1 = False
condicao2 = False
condicao3 = True
condicao4 = False

if condicao1:
    print('Este código será exibido caso a condicao1 seja verdadeira')
elif condicao2:
    print('Este código será exibido caso a condicao2 seja verdadeira')
elif condicao3:
    print('Este código será exibido caso a condicao3 seja verdadeira')
elif condicao4:
    print('Este código será exibido caso a condicao4 seja verdadeira')
else:
    print('Se nenhuma das condicões anteriores forem verdadeiras, isso será exibido')